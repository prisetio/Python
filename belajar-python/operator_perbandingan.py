# Operator Perbandingan

# > Lebih dari
# < Kurang dari
# >= Lebih dari sama dengan
# <= Kurang dari sama dengan
# == Sama dengan
# != Tidak sama dengan

print(5 <= 3)
print(10 == 10)
print(7 != 9)

# Keunikan dari sama dengan (==) dan tidak sama dengan (!=)
print("priyo" == "priyo")
print("priyo" != "priyo")